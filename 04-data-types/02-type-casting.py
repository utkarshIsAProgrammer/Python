# Integer conversion
x = int(1)
y = int(2.8)
z = int("3")

print(x, type(x))
print(y, type(y))
print(z, type(z))

# Float conversion
x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")

print(x, type(x))
print(y, type(y))
print(z, type(z))
print(w, type(w))

# String conversion
x = str("s1")
y = str(2)
z = str(3.0)

print(x, type(x))
print(y, type(y))
print(z, type(z))
