# Making a variable
name = "Panchajanya"
age = 19
role = "Fullstack Developer"

print(name)
print(age)
print(role)

# Get data type of variable
print(type(name))
print(type(age))
print(type(role))

# Multiple variable in one line
a, b, c, d = 5, 6, 7, 8
print(a, b, c, d)

# One value to multiple variables
x = y = z = 9
print(x, y, z)

# Unpack a collection
fruits = ["pineapple", "mango", "guava", "banana"]
one, two, three, four = fruits
print(one, two, three, four)

# Concatenation
print(
    one + " + " + two + " + " + three + " + " + four + " = perfect fruit salad for me."
)
